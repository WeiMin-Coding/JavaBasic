public class Exercise02 {
    public static void main(String[] args) {
        // 实现判断一个整数，属于哪个范围：大于0，小于0，等于0
        int number = 0;
        if (number > 0) {
            System.out.println("大于0");
        } else if (number < 0) {
            System.out.println("小于0");
        } else if (number == 0) {
            System.out.println("等于0");
        }
    }
}
